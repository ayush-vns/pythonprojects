# Control Structures
# If else is the simplest control structure where one portion of code is run if a condition is True, the other if
# the condition is False
condition = True
if condition:
    print(1)
    print(2)
else:
    print(3)
    print(4)
print()
condition = False
if condition:
    print(1)
    print(2)
else:
    print(3)
    print(4)

a, b = 1, 2
if a <= b:
    print("Min is ", a)
else:
    print("Min is ", b)


if a >= b:
    print("Max is ", a)
else:
    print("Max is ", b)
